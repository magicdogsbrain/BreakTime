/**
 * Get N random questions that haven't been asked recently
 */
export function getQuizQuestions(count = 10, excludeIds = []) {
  const available = BRITPOP_QUESTIONS.filter(q => !excludeIds.includes(q.id));
  const shuffled = available.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Check answer
 */
export function checkAnswer(question, answer) {
  return question.answer === answer;
}

/**
 * Get all available question IDs (for tracking)
 */
export function getAllQuestionIds() {
  return BRITPOP_QUESTIONS.map(q => q.id);
}
